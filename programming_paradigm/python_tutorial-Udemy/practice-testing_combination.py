# Practice2: A testing combination app using for loop
meal = ["Pizza", "Speghetti", "Burger"]
drink = ["Water", "Juice", "cola"]
dessert = ["Cake", "Ice cream", "Chips"]

for m in meal:
    for d in drink:
        for ds in dessert:
            print(f"I will have {m} as a meal, {d} as a drink and {ds} as a dessert.")

