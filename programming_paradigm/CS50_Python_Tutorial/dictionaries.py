# Dictionaries in python

person_details = {"f_name": "John", "l_name": "Doe", "age": 30, "address": "Logan Town", "is_married": True}
print(person_details)

print("---------------------------------------")

print(person_details["f_name"])

print("---------------------------------------")

person_details["religion"] = "Christian"
print(person_details)

print("---------------------------------------")
print(person_details.keys())
print("---------------------------------------")
print(person_details.values())

